
public class Medicine {
	private int cost;
	private int noma;
	public Medicine(int cost, int noma) {
		this.cost = cost;
		this.noma = noma;
	}
	public int getCost() {
		return cost;
	}
	public void setCost(int cost) {
		this.cost = cost;
	}
	public int getNoma() {
		return noma;
	}
	public void setNoma(int noma) {
		this.noma = noma;
	}
}
